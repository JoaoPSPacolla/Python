import math

pi = 3.14
x = 1
y = 2
z = 3

print(round(pi)) #arredonda para cima
print(math.ceil(pi)) #arredonda para baixo
print(math.floor(pi))
print(abs(pi)) #how close the number is to zero = módulo
print(pow(pi,2))#pi to the power of 2
print(math.sqrt(4)) #square root
print(min(x,y,z))
print(max(x,y,z))